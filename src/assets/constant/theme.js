export const judgeUrlIsTemplate = (url) => {
  return url.includes('templateMaterial/screen');
};

export const getScreenTemplateMaterialType = (url) => {
  let tmpType = 0;
  if (url.includes('templateMaterialsTheme')) {
    tmpType = 0;
  } else if (url.includes('templateMaterialsFestival')) {
    tmpType = 1;
  }
  return tmpType;
};

export const formatScreenUrl = (url) => {
  // 将ustatic.hudongmiao.com替换为static.hudongmiao.com
  return url.replace('ustatic.hudongmiao.com', 'static.hudongmiao.com');
};
